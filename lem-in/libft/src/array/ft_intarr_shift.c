void		ft_intarr_shift(int *arr, int len)
{
	int	i;
	int	tmp;

	tmp = arr[len - 1];
	i = len - 1;
	while (i > 0)
	{
		arr[i] = arr[i - 1];
		i--;
	}
	arr[0] = tmp;
}
