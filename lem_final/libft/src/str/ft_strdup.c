#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*dup;
	int		size;
	int		i;

	i = 0;
	size = ft_strlen(src);
	if (!(dup = (char*)malloc(sizeof(*dup) * (size + 1))))
		return (NULL);
	while (src[i] != '\0')
	{
		dup[i] = src[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
