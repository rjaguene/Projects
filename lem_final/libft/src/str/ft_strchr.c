#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t		i;

	i = 0;
	while (c == '\0' || s[i])
	{
		if (s[i] == (char)c)
			return ((char *)s + i);
		i = i + 1;
	}
	return (0);
}
